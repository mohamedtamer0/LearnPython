# Back Space
print("Hello\bWorld")  # Will Remove o

# Escap New Line + \
print("Hello \
I Love \
Python")


# Escap Back Slash
print("I Love Python \\")

# Escap Single Quote
print('I Love \'Python\'')

# Escap Double Quote
print("I Love \"Python\"")


# Line Feed Or New Line
print("Hello My name Is Mohamed\nMy Age 25")


# Carriage Return
print("123456\rAbcde")









